#実験1 銅の電気抵抗の温度依存性(室温以上)
import numpy as np
import matplotlib.pyplot as plt

#温度と抵抗のフィット
a = 300 / (149.51 - 28.76)
b = 100 - a * 28.76

#正電流の測定
rp = [109.9884, 114.0388, 118.0061, 122.438, 126.309, 130.203, 134.011, 137.932, 141.803, 145.618, 149.519]
V2p = [1344.38, 1088.08, 803.433, 533.309, 388.368, 273.298, 198.282, 144.479, 106.92, 79.2825, 59.3600]
V4p = [556.954, 450.838, 332.186, 228.084, 159.723, 112.151, 81.1604, 59.0393, 43.4440, 32.1038, 23.9170]
#オームとmV

rp = np.array(rp)
V2p = np.array(V2p)
V4p = np.array(V4p)
Tp = a * rp + b

#負電流
rm = [109.9878, 114.1004, 118.1124, 122.541, 126.381, 130.261, 134.055, 137.993, 141.856, 145.672, 149.574]
V2m = [1401.22, 1120.011, 815.908, 556.417, 389.546, 273.675, 198.336, 71.977, 106.6278, 79.0841, 59.2016]
V4m = [556.563, 448.610, 329.080, 225.777, 158.545, 111.571, 80.7859, 58.7716, 43.2428, 31.9565, 23.7950]

rm = np.array(rm)
V2m = - np.array(V2m)
V4m = - np.array(V4m)
Tm = a * rm + b

#熱起電力の補正
rho2 = (V2p - V2m) / 2 * (2.32 / (1.01 * 1))
rho4 = (V4p - V4m) / 2 * (2.32 / (1.01 * 1))
T = (Tp + Tm) / 2

plt.scatter(T, rho2, label= "2tanshi")
plt.scatter(T, rho4, label= "4tanshi")
plt.xlim(290, 410)
plt.ylim(0, 1400)
plt.legend()
plt.show()